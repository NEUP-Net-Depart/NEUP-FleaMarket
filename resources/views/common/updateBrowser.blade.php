<h2>为什么要升级浏览器</h2>
<p>旧版浏览器缺乏可能的关键浏览器安全更新，如果你继续使用它们，你将可能受到病毒、间谍软件和其他恶意软件的攻击，无法确保个人信息的安全。请参阅 <a href="https://www.microsoft.com/zh-cn/windowsforbusiness/end-of-ie-support">Microsoft 关于对 Internet Explorer 早期版本的支持结束的说明</a> 。</p>
<div class="line"></div>
