<h1>请升级你的浏览器</h1>
<p>当你被引导到这个页面时，你正在使用一个<strong>过时的</strong>浏览器。这意味着在升级浏览器前，你将无法正常使用此网站的部分甚至全部功能。</p>
<div class="line"></div>
<h2>为什么会出现这个页面</h2>
<p>你的浏览器被检测出低于网站正常运行全部功能所要求的最低版本。如果你使用的是 IE6/7/8/9/10，或你的浏览器已经是最新，请考虑更换为以下浏览器。如果你使用的是下列浏览器，请升级为最新版本。</p>
<div class="line"></div>
